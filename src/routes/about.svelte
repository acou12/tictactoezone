<h1>About</h1>
<h2>What is TicTacToeZone™?</h2>
<p>
  TicTacToeZone™ is an online TicTacToe® server designed for seamless play,
  analysis, and discussion. The game of TicTacToe® was invented by
  TicTacToeZone™ in 2017, and it's complex ruleset has been developed and
  improved since. TicTacToeZone™ now presents the game to the world and
  challenges people to analyse its unique strategy and try to become the
  <em>TicTacToe Master©!</em>
</p>
<h2>Awesome! How do I get started?</h2>
<p>
  All you need to do is visit the <a href="/new">new game page</a> to create a new
  game. This will give you a sharable link to send to friends or opponents, which
  when visited will start the game.
</p>
